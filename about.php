    <h2>Tentang Fakultas Teknik Universitas Hasanuddin </h2>
    <p>Sejarah Singkat Fakultas Teknik (FT) adalah salah satu fakultas pada Universitas Hasanuddin (UNHAS) Makassar, berdiri tahun 1960. Ketika itu kampus UNHAS masih berlokasi di Baraya. Misi utama pendirian Fakultas Teknik pada masa itu adalah memberi
        peluang kepada putera-puteri yang berasal dari daerah untuk menempuh jenjang pendidikan tinggi dalam rangka memenuhi kebutuhan tenaga ahli bidang Teknik. Pada mulanya kurikulum Fakultas Teknik hanya merupakan kurikulum tunggal dengan beberapa
        matakuliah pilihan yang terbatas. Kurikulum ini mengalami berbagai perubahan dan perkembangan sejak tahun 1963, termasuk peralihan dari Program Sarjana Muda, Program Sarjana 6 tahun, sampai ke Program Strata satu.</p>
    <p>
        Pada tahun 1985, Fakultas Teknik UNHAS pindah kampus ke Tamalanrea. Fakultas Teknik menempati lantai 1 gedung dan sebagian gedung Heavy Laboratory. Bersamaan dengan itu, dilaksanakanlah pengadaan peralatan laboratorium yang baru. Selama sepuluh tahun
        dari tahun 1986 sampai tahun 1996, Fakultas Teknik mendapatkan Proyek Percepatan Insinyur (PPI) yang bertujuan meningkatkan produktivitas kelulusan. Pada awalnya Fakultas Teknik Universitas Hasanuddin pada tanggal 10 September 1960 hanya terdapat
        3 jurusan, yakni Teknik Sipil, Teknik Mesin dan Teknik Perkapalan. Selanjutnya Fakultas Teknik mempunyai 6 jurusan dengan membuka Jurusan Teknik Elektro, Teknik Arsitektur dan Teknik Geologi dengan total program studi sebanyak 23.
    </p>
    <p>
        Seiring perkembangan, Fakultas Teknik Unhas maka dibangun pula Fakultas Teknik yang berada di Kampus Teknik Gowa yang saat ini telah diresmikan oleh Wakil Presiden RI, Bapak Jusuf Kalla pada tanggal 23 Juni 2018. Saat ini Fakultas Teknik memiliki 13 Departemen
        Sarjana, 6 Program Studi Magister , 5 Program Studi Doktor dan 1 Program Studi Pendidikan Profesi Insinyur (PPI).
    </p>
